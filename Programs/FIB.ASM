ASSUME DS:DATA,CS:CODE
DATA SEGMENT
	NUM DB 0AH
	RES DB 0AH DUP(?)
DATA ENDS

CODE SEGMENT
START: MOV AX,DATA
	MOV DS,AX
	MOV CH,00H
	MOV CL,NUM
	MOV SI,OFFSET RES
	MOV BX,00H
	MOV DX,01H
	MOV [SI],BX
	INC SI
	MOV [SI],BX
	INC SI
	MOV [SI],DX
	DEC CX
        DEC CX
BEGIN: MOV AX,00H
        ADD AX,BX
        ADD AX,DX
        DAA
        INC SI
        MOV [SI],AX
        MOV BX,DX
        MOV DX,AX
        LOOP BEGIN
        MOV AH,4CH
        INT 21H
 CODE ENDS
 END START
